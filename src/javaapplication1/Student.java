    private void cancelBTN1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancelBTN1ActionPerformed
       int phonelength=phoneTB.getText().length();
       if (phonelength==10){
        try{
         stmt = con.createStatement();
         int stdId = Integer.parseInt(idTB.getText());
         String stdName=nameTB.getText();
         String bloodType=bloodTB.getText();
         String parentName=parentTB.getText();
         String city=cityTB.getText();
         String phone=phoneTB.getText();
         int stdClass=Integer.parseInt(classTB.getText());
         
         String sql =
                 "UPDATE STUDENT SET std_Name='"+stdName+"',std_FatherName='"+parentName+"',std_BloodType='"+bloodType+"'"
                 + ",std_Phone='"+phone+"',std_City='"+city+"',std_Class='"+stdClass+"' WHERE ID='"+stdId+"'";
                 
         
         stmt.executeUpdate(sql);
         JOptionPane.showMessageDialog(null,"Data Updated");
              
                
         
    }catch(SQLException | HeadlessException | NumberFormatException e){
        JOptionPane.showMessageDialog(null,e);
         
    }
       }else
           JOptionPane.showMessageDialog(null,"Invalid PhoneNumber, Must be 10 digits");
    }//GEN-LAST:event_cancelBTN1ActionPerformed

