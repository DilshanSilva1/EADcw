    private void SearchBTN1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchBTN1ActionPerformed
        try{
            stmt = con.createStatement();
            int stdId = Integer.parseInt(idTB.getText());
            String sql = "SELECT * FROM student WHERE id = '"+stdId+"'";
            rs= stmt.executeQuery(sql);
            if (rs.next()){
                nameTB.setText(rs.getString("std_Name"));
                parentTB.setText(rs.getString("std_FatherName"));
                cityTB.setText(rs.getString("std_City"));
                bloodTB.setText(rs.getString("std_BloodType"));
                phoneTB.setText(rs.getString("std_Phone"));
                classTB.setText(String.format("%s",rs.getInt("std_Class")));
            }else{
                JOptionPane.showMessageDialog(null, "Record Not Found");
            }
            
           
            
        }catch(NumberFormatException | SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
        
    }//GEN-LAST:event_SearchBTN1ActionPerformed

